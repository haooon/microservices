package com.haooon.servicesys.params;

public class HParamOPRecord {
    private String op_user;
    private String op_type;
    private String op_content;
    private String op_timestamp;

    public String getOp_user() {
        return op_user;
    }

    public void setOp_user(String op_user) {
        this.op_user = op_user;
    }

    public String getOp_type() {
        return op_type;
    }

    public void setOp_type(String op_type) {
        this.op_type = op_type;
    }

    public String getOp_content() {
        return op_content;
    }

    public void setOp_content(String op_content) {
        this.op_content = op_content;
    }

    public String getOp_timestamp() {
        return op_timestamp;
    }

    public void setOp_timestamp(String op_timestamp) {
        this.op_timestamp = op_timestamp;
    }
}
